var Date = []string{"Mon", "Tue", "Wed", "Thu", "Fri", "Sat", "Sun"}

var DaysToBuild = map[string]int{
	"Mon":       1,
	"monday":    1,
	"Tue":       1 << 1,
	"tuesday":   1 << 1,
	"Wed":       1 << 2,
	"wednesday": 1 << 2,
	"Thu":       1 << 3,
	"thursday":  1 << 3,
	"Fri":       1 << 4,
	"friday":    1 << 4,
	"Sat":       1 << 5,
	"saturday":  1 << 5,
	"Sun":       1 << 6,
	"sunday":    1 << 6,
	"all":       127,
}

// DaysToDate days to date
// 1 -> [Mon]
// 31 -> [Mon, Tue, Wed, Thu, Fri]
func DaysToDate(dayToBuild int) []string {
	var days []string
	for _, day := range Date {
		if dayToBuild&DaysToBuild[day] == DaysToBuild[day] {
			days = append(days, day)
		}
	}
	return days
}

// DateToDays date to days
// [Mon] -> 1
// [Tue] -> 2
// [Mon, Tue] -> 3
// [Mon, Tue, Wed, Thu, Fri] -> 31
func DateToDays(date []interface{}) int {
	daysToBuild := 0
	for _, day := range date {
		daysToBuild += DaysToBuild[day.(string)]
	}
	return daysToBuild
}

